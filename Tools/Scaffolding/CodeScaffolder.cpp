static const char* kHeaderTemplate = R"(#pragma once
// ${CLASS_NAME} — ${DESCRIPTION}

#include <string>

namespace ${NAMESPACE} {

class ${CLASS_NAME} {
public:
    ${CLASS_NAME}();
    ~${CLASS_NAME}();

    void Init();
    void Shutdown();
    void Update(float dt);

private:
};

} // namespace ${NAMESPACE}
)";

static const char* kSourceTemplate = R"(#include "${INCLUDE_PATH}"

namespace ${NAMESPACE} {

${CLASS_NAME}::${CLASS_NAME}()  {}
${CLASS_NAME}::~${CLASS_NAME}() {}
void ${CLASS_NAME}::Init()             {}
void ${CLASS_NAME}::Shutdown()         {}
void ${CLASS_NAME}::Update(float /*dt*/) {}

} // namespace ${NAMESPACE}
)";

static const char* kCMakeTemplate = R"(add_library(${LIBRARY_NAME} STATIC
    ${CLASS_NAME}/${CLASS_NAME}.cpp
)
target_include_directories(${LIBRARY_NAME} PUBLIC ${CMAKE_SOURCE_DIR})
target_compile_features(${LIBRARY_NAME} PUBLIC cxx_std_20)
)";

static const char* kAgentHeaderTemplate = R"(#pragma once
// ${CLASS_NAME} — AI Agent (generated by CodeScaffolder)

#include "AI/Orchestrator/Orchestrator.h"
#include <string>

namespace AI {

class ${CLASS_NAME} {
public:
    ${CLASS_NAME}();

    /// Register this agent with an Orchestrator.
    void Register(AI::Orchestrator& orchestrator);

    /// Execute a job assigned to this agent.
    bool Execute(AI::OrchestratorJob& job);

private:
    std::string m_name = "${CLASS_NAME}";
};

} // namespace AI
)";

// ─────────────────────────────────────────────────────────────────────────────
// TemplateEngine
// ─────────────────────────────────────────────────────────────────────────────

TemplateEngine::TemplateEngine(const std::string& root)
    : m_root(root)
{}

std::string TemplateEngine::Load(const std::string& name) const
{
    // Check inline first
    auto it = m_inline.find(name);
    if (it != m_inline.end()) return it->second;

    // Try file system
    if (!m_root.empty()) {
        std::ifstream f(m_root + "/" + name);
        if (f) {
            return std::string(std::istreambuf_iterator<char>(f),
                               std::istreambuf_iterator<char>());
        }
    }
    return {};
}

/* static */
std::string TemplateEngine::Render(
    const std::string& tmpl,
    const std::unordered_map<std::string,std::string>& vars)
{
    std::string result = tmpl;
    for (const auto& [key, val] : vars) {
        std::string token = "${" + key + "}";
        size_t pos = 0;
        while ((pos = result.find(token, pos)) != std::string::npos) {
            result.replace(pos, token.size(), val);
            pos += val.size();
        }
    }
    return result;
}

void TemplateEngine::RegisterInline(const std::string& name,
                                     const std::string& content)
{
    m_inline[name] = content;
}

